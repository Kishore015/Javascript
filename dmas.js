function divide(a,b){
    return a/b;
}
function multiply(a,b){
    return a-b;
}
function add(a,b){
    return a+b;
}
function substract(a,b){
    return a-b;
}
function reminder(a,b){
    return a%b;
}

console.log("Divide",divide(5,10))
console.log("Multiply",multiply(5,10))
console.log("Add",add(5,10))
console.log("Subtract",substract(5,10))
console.log("Subtract",reminder(5,10))